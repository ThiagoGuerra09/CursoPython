'''
Operadores aritméticos
+ adição
- subtração
* multiplicação
/ divisão
// divisão inteira
** potência
% pega o resto da divisão 
() altera a ordem de execução, visto que na matemática, multiplicação e divisão tem prioridade na
execução
'''
#exemplos
print(2**10)#2^10
print(10//3)# retorna o valor inteiro
print(10/3)# retorna valor real
print(10%3)# retorna o resto
print((10+5)*2)# sem os parenteses é 20, com os parenteses é 30

